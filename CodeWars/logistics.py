def getTotalImbalance(weight):
    imbalance = 0
    if min(weight) == max(weight):
        return imbalance
    for chunks in range(2, len(weight) + 1):
        for i in range(len(weight)):
            if i + chunks <= len(weight):
                weights = weight[i : i + chunks]
                imbalance += max(weights) - min(weights)

    return imbalance


